function productCardHtml(product) {
    return `
        <div class="card">
            <div class="img-wrapper">
                <img src="${product.main_image}" alt="${product.title}" />
            </div>
            
            <h3>${product.title}</h3>
            <p class="price">${product.price}</p>
            <p class="description">${product.description}</p>

            <button class="add-cart-btn" data-id="${product.id}">
                Add to Cart
            </button>
        </div>
    `;
}




const prefixToSection = {
    "11": "c.xbox",
    "12": "c.playstation",
    "21": "controllersSection",
    "22": "gripsSection",
    "23": "headsetsSection",
    "24": "gpusSection",
    "31": "xboxGamesSection",
    "32": "playStationGamesSection",
    "33": "pcGamesSection"
};

// single pass: create card elements and append to matching sections
products.forEach(p => {
    const prefix = String(p.id).slice(0, 2);
    const sectionId = prefixToSection[prefix];
    if (!sectionId) return;                 // no mapping -> skip
    const container = document.getElementById(sectionId);
    if (!container) return;                 // missing container -> skip
    const card = productCardHtml(p);
    container.insertAdjacentHTML("beforeend",card);
});
//pop the message
function showPopup(msg) {
    const popup = document.getElementById("popup-msg");
    popup.innerHTML = msg;
    popup.style.opacity = 1;

    setTimeout(() => {
        popup.style.opacity = 0;
    }, 1000); // jitne seconds utne hazar
}

